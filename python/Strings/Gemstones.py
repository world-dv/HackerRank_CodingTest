def gemstones(arr):
    ar = dict()
    total = []
    for i in arr:
        total += list(set(i))
    for i in total:
        ar[i] = total.count(i)
    answer = [i for i in ar if ar[i] == len(arr)]
    return len(answer)
        

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    arr = []

    for _ in range(n):
        arr_item = input()
        arr.append(arr_item)

    result = gemstones(arr)

    fptr.write(str(result) + '\n')

    fptr.close()
