def alternatingCharacters(s):
    start = 0
    count = 0
    for i in s:
        if start == 0:
            start = i
            continue
        if start == i:
            count += 1
        if start != i:
            start = i
    return count

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    q = int(input().strip())

    for q_itr in range(q):
        s = input()

        result = alternatingCharacters(s)

        fptr.write(str(result) + '\n')

    fptr.close()
