def viralAdvertising(n):
    shared = 5
    linked = 2
    answer = linked
    for i in range(0, n - 1):
        shared = (shared // 2) * 3
        linked = shared // 2
        answer += linked
    return answer

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    result = viralAdvertising(n)

    fptr.write(str(result) + '\n')

    fptr.close()
