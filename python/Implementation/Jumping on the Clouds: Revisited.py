# Complete the jumpingOnClouds function below.
def jumpingOnClouds(c, k):
    e = 0
    idx = 0
    while True:
        idx = (idx + k) % len(c)
        if c[idx]:
            e -= 3
        else:
            e -= 1
        if idx == 0:
            break
    return e + 100
        

